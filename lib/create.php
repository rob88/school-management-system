   if($_POST){
    
       // include database connection
       include '../config/db_connector.php';
    
       try{  //// try to insert the below values into the database
        
           // insert query ////////////////////////////////////////////////////////////////////////
           $query = "INSERT INTO school_ SET name=:name, email=:email, school_name=:school_name, created=:created"; ////////////////////////////////////////////////////////////////
           ///////////////////////////////////////////////////////////////////////////////////////
   
   
           // prepare query for execution ///////////////////////////////////////////////////////
           $stmt = $con->prepare($query); ///////////////////////////////////////////////////////
           //////////////////////////////////////////////////////////////////////////////////////
   
   
   
           // posted values //////////////////////////////////////////////////////////////////////
           $name=htmlspecialchars(strip_tags($_POST['name'])); ///////////////////////////////////
           $email=htmlspecialchars(strip_tags($_POST['email'])); /////////////////////////////////
           $school_name=htmlspecialchars(strip_tags($_POST['school_name'])); /////////////////////
           ///////////////////////////////////////////////////////////////////////////////////////
   
   
           // bind the parameters  ///////////////////////////////////////////////////////////////
           $stmt->bindParam(':name', $name); /////////////////////////////////////////////////////
           $stmt->bindParam(':email', $email); ///////////////////////////////////////////////////
           $stmt->bindParam(':school_name', $school_name); ///////////////////////////////////////
           ///////////////////////////////////////////////////////////////////////////////////////
   
   
           // specify when this record was inserted to the database//////////////////////////////
           $created=date('Y-m-d H:i:s'); ////////////////////////////////////////////////////////
           $stmt->bindParam(':created', $created); //////////////////////////////////////////////
           ///////////////////////////////////////////////////////////////////////////////////////
   
   
           // Execute the query/////////////////////////////////////////////////////////////////////////
           if($stmt->execute()){                    ////////////////////////////////////////////////////
               header('refresh:2;url= ..\read.php');////////////////////////////////////////////////////
               echo"<center><h2>Saving Your Record Please Wait..... </h2></center><br/>"."<center><img src='../img/saving.gif'></center>";
                echo "<div class='alert alert-success'><a href='../read.php'>Home</a></div>"; //////////
           }else{                                        ///////////////////////////////////////////////
                                                         ///////////////////////////////////////////////
               echo"<a href='../read.php'>Home</a></div>";//////////////////////////////////////////////
               echo "<div class='alert alert-danger'>Unable to save record.</div>";/////////////////////
           }//end else
            
       }//end try   //////////////////////////////////////////////////////////////////////////////////
       ///////////////////////////////////////////////////////////////////////////////////////////////
      
   
       // show error ////////////////////////////////////////////////////////////////////////////////
       catch(PDOException $exception){
           die('ERROR: ' . $exception->getMessage());
       }
   }
   
   ?>